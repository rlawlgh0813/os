int
sys_sbrk(void)
{
  int addr;
  int n;

  if(argint(0, &n) < 0)
    return -1;
  addr = myproc()->sz;
  if(growproc(n) < 0)
    return -1;
  return addr;
}

int
sys_sleep(void)
{
  int n;
  uint ticks0;

  if(argint(0, &n) < 0)
    return -1;
  acquire(&tickslock);
  ticks0 = ticks;
  while(ticks - ticks0 < n){
    if(myproc()->killed){
      release(&tickslock);
      return -1;
    }
    sleep(&ticks, &tickslock);
  }
  release(&tickslock);
  return 0;
}

// return how many clock tick interrupts have occurred
// since start.
int
sys_uptime(void)
{
  uint xticks;

  acquire(&tickslock);
  xticks = ticks;
  release(&tickslock);
  return xticks;
}

// New system call for project 2
int
sys_settickets(void)
{
  int tickets, end_ticks;
  struct proc *p = myproc();

  // Invalid arguments
  if(argint(0, &tickets) < 0 || argint(1, &end_ticks) < 0) return -1; 
  // Invalid num of tickets
  if(tickets < 1 || tickets > (STRIDE_MAX -1)) return -1;

  // Set the number of tickets and end_ticks for the process
  p->tickets = tickets;
  p->stride = STRIDE_MAX / tickets;

  // Set end_ticks only if it is valid
  if(end_ticks >= 1) p->end_ticks = end_ticks;
  
  return 0;
}